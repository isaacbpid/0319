  const normalizeMakeName = (value: string): string => {
    return value
      .toLowerCase()
      .replace(/\([^)]*\)/g, ' ')
      .replace(/[^a-z0-9\u4e00-\u9fff]+/g, ' ')
      .trim();
  };

  const makeLookupCandidates = (value: string): string[] => {
    const trimmed = value.trim();
    if (!trimmed) return [];

    const base = normalizeMakeName(trimmed);
    const firstToken = normalizeMakeName(trimmed.split(' ')[0] || '');
    return Array.from(new Set([base, firstToken].filter(Boolean)));
  };

  const getEnglishMakeAlias = (key: string): string => {
    const englishPart = key.replace(/[\u4e00-\u9fff].*$/, '').trim();
    return englishPart || key.trim();
  };

  export const VEHICLE_MAKES_EXTENDED: string[] = Object.keys(VEHICLE_MODELS_BY_MAKE)
    .map((key) => key.trim())
    .filter(Boolean)
    .sort((a, b) => a.localeCompare(b));

  export const getVehicleModelsForMake = (make: string): string[] => {
    const candidates = makeLookupCandidates(make);
    if (candidates.length === 0) return [];

    const matchedKey = Object.keys(VEHICLE_MODELS_BY_MAKE).find((key) => {
      const keyCandidates = makeLookupCandidates(key);
      const englishAlias = getEnglishMakeAlias(key);
      const englishCandidates = makeLookupCandidates(englishAlias);
      return [...keyCandidates, ...englishCandidates].some((candidate) => candidates.includes(candidate));
    });

    if (!matchedKey) return [];

    const normalizeModelLabel = (value: string): string => {
      // Keep model only and strip trailing type labels like "| SUV ...".
      return value.split('|')[0].trim();
    };

    const normalizeModelKey = (value: string): string => normalizeMakeName(value).replace(/\s+/g, '');

    const byKey = new Map<string, string>();
    for (const rawModel of VEHICLE_MODELS_BY_MAKE[matchedKey] || []) {
      const model = normalizeModelLabel(rawModel);
      if (!model) continue;
      const key = normalizeModelKey(model);
      const existing = byKey.get(key);
      if (!existing || model.length < existing.length) {
        byKey.set(key, model);
      }
    }

    return Array.from(byKey.values());
  };